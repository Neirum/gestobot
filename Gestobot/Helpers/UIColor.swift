import UIKit.UIColor


extension UIColor {
    
    class var lightOrange : UIColor {
        return UIColor(red: 255 / 255.0, green: 204 / 255.0, blue: 100 / 255.0, alpha: 1)
    }
    
    class var darkBlue : UIColor {
        return UIColor(red: 120 / 255.0, green: 157 / 255.0, blue: 173 / 255.0, alpha: 1)
    }

}
